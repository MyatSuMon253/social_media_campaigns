                        <h3 class="mb-4">How to Stay Safe on livestreaming</h3>

                        <p>With all social media, there comes risk. Risk associated with privacy, data and vulnerabilities. Where videos could once be censored and taken down, when acts unfold live over the internet, it is often a lot less containable. Most recently, there have been some horrific and disturbing results, with murder, rape and other horrific violent crimes occurring in real-time on live streaming channels.
                        </p>

                        <p>It is not only the threat of young people being exposed to uncensored and horrific content that live streaming poses, but there are risks as well to those who are shown on live streaming videos that need to be considered. For instance:</p>

                        <blockquote>
                            the danger of self broadcasting
                        </blockquote>

                        <p>
                            <strong>Issue of Consent: </strong>When live streaming, it is broadcast instantly for all to see, so can quickly involve those who do not want to be included or have not given their consent.
                        </p>
                        <p>
                            <strong>Accidentally releasing their location: </strong>For instance, if a teen live streams on their street, everyone will know where they live.
                        </p>
                        <p>
                            <strong>Revealing intimate information: </strong>Live streaming could lead to young people revealing too much personal information about themselves. Even publishing live video in their own bedrooms could give away clues to their identity or other personal information.
                        </p>
                        <p>
                            <strong>Not knowing who is watching: </strong>Live streaming services have limited privacy controls, and so it is hard to know who is watching and prevent people accessing the stream.
                        </p>
